package com.tema.model;

public class Manager {
	
	private String name;
	private String team;
	private String football_gender;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getFootball_gender() {
		return football_gender;
	}
	public void setFootball_gender(String football_gender) {
		this.football_gender = football_gender;
	}
	
	

}
